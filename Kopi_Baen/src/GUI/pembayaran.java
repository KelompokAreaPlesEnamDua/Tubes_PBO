    private void txtJumlahdibayarkanKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtJumlahdibayarkanKeyTyped
        // TODO add your handling code here:
        char enter = evt.getKeyChar();
        if(Character.isAlphabetic(enter)){
            evt.consume();
            txtKembalian.setText("Inputan harus angka");
            lblkem.setVisible(false);

        } 
    }//GEN-LAST:event_txtJumlahdibayarkanKeyTyped

    private void txtJumlahdibayarkanKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtJumlahdibayarkanKeyReleased
        // TODO add your handling code here:
        if (!txtJumlahdibayarkan.getText().equalsIgnoreCase("")) {
            bayar = Integer.parseInt(txtJumlahdibayarkan.getText());
            total = Integer.parseInt(lblTotal.getText());
            kembalian = bayar-total;
            
            if (kembalian < 0) {
                lblkem.setVisible(false);
                txtKembalian.setText("Jumlah uang kurang");
                btnBayar.setEnabled(false);
            }else{
                lblkem.setVisible(true);
                txtKembalian.setText(String.valueOf(+kembalian));
                btnBayar.setEnabled(true);

            }
        }
    }//GEN-LAST:event_txtJumlahdibayarkanKeyReleased

