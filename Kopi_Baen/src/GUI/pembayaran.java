    public void jam()
    {
        Timer t = new Timer();
        t.scheduleAtFixedRate(new TimerTask(){
            
            @Override
            public void run(){
                lblJam.setText(new SimpleDateFormat("HH:mm:ss").format(new java.util.Date()));
            }
        },0,1000);
    }
    
    private void tampil()
    {
        DTM = (DefaultTableModel) tblBayar.getModel();
        DTM.setRowCount(0);
        // RAI
        try {
            if (strans.cekId(txtCari.getText())) {
                JOptionPane.showMessageDialog(null, "ID " +txtCari.getText()+" SUDAH DIBAYAR");
                txtCari.setText(null);
                return;
            }
        } catch (SQLException ex) {
            Logger.getLogger(pembayaran.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        strans.tampil(DTM, txtCari.getText());
        tblBayar.setModel(DTM);
        if (DTM.getRowCount() > 0) {
            total();
            txtIDOrder.setText(txtCari.getText());
            txtJumlahdibayarkan.setEnabled(true);
            namaCust = DTM.getValueAt(0, 2).toString();
        } else {
            txtIDOrder.setText(null);
            lblTotal.setText(null);
            txtJumlahdibayarkan.setText(null);
            txtJumlahdibayarkan.setEnabled(false);
            txtKembalian.setText(null);
            btnBayar.setEnabled(false);
        }
    }
    
    private void total() 
    {
        try { 
//            ResultSet RS = strans.total(tblBayar.getValueAt(tblBayar.getSelectedRow(), 1).toString());
            ResultSet RS = strans.total(txtCari.getText());
            if (RS.next()) {
                lblTotal.setText(String.valueOf(RS.getInt("total")));
            }  
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Error: Gagal");
        }
    }
    
    private void simpanData()
    {
        idUser = txtIDAdmin.getText();
        idOrder = txtIDOrder.getText();
        tanggal = lblTanggal.getText();
        total = Integer.parseInt(lblTotal.getText());
        
        strans.simpandata(idUser, idOrder, namaCust, tanggal, total);
        JOptionPane.showMessageDialog(null, "ID " +txtIDOrder.getText()+" TERBAYAR");
        btnBayar.setEnabled(false);
        
    }
    
    private void reset()
    {
        DefaultTableModel model = (DefaultTableModel) tblBayar.getModel();
        model.setRowCount(0);
        txtCari.setText(null);
        txtIDOrder.setText(null);
        lblTotal.setText(null);
        txtJumlahdibayarkan.setText(null);
        txtJumlahdibayarkan.setEnabled(false);
        txtKembalian.setText(null);
        btnBayar.setEnabled(false);
        txtCari.requestFocus();
    }
